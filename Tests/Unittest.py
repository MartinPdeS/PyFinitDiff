from PyFinitDiff.Source import FiniteDifference2D
from SuPyMode.Materials         import FusedSilica

from SuPyMode.Geometry          import Geometry, Fused4, Circle
import numpy as np
import matplotlib.pyplot as plt

from numpy import linalg as LA
from scipy.sparse import linalg
Eigs = linalg.eigs

np.set_printoptions(edgeitems=100, linewidth=100000, sign=' ', formatter={'all': lambda x : f'{x:f}'})



Nx = 5
Ny = 5
Size = Nx*Ny


Clad = Circle(Position=(0,0), Radius = 190, Index = 2)


Geo = Geometry( Clad    = Clad,
                Objects = [],
                Xbound  = [0, 230],
                Ybound  = [-230, 230],
                Nx      = Nx,
                Ny      = Ny )

Geo.CreateMesh()
# Geo.Axes.dx = Geo.Axes.dy = 1

print(Geo.Axes.dx, Geo.Axes.dy)

Plot = True



a = FiniteDifference2D(Nx=Nx,
                       Ny=Ny, 
                       dx=Geo.Axes.dx, 
                       dy=Geo.Axes.dy, 
                       Derivative=2, 
                       Accuracy=2)

a.Boundary.Top = 0
a.Boundary.Bottom = 0
a.Boundary.Right = 0
a.Boundary.Left = 1
a.Compute()

print(a.Dense)



b = a.Dense
np.fill_diagonal(b, -b.diagonal() + Geo.Mesh.T.flatten())





if Plot:
	EigenValues, EigenVectors = Eigs(b, k=5)

	fig, axes = plt.subplots(1,EigenValues.size, figsize=(10, 5))

	for i, ax in enumerate(axes[:-1]):
		Vector = EigenVectors.T[i].real.reshape([Ny, Nx]).T
		Vector = np.flip(Vector, -1)
		ax.imshow(Vector.T)
	
	axes[-1].imshow(Geo.Mesh.T)
	plt.show()


